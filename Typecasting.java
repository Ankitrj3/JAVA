public class Typecasting {
    public static void main(String args []){
        int a = 234;
        System.out.println(a);

        double i = (double)a; //typecasting
        System.out.println(i);

        double k = 34.56;
        System.out.println(k);
        
        int j = (int)k;
        System.out.println(j);

    }
}
